				<h2 class="gris3">Método Suzuky</h2>
				<br />
				
	
				<div class="texto gris2">
					Shinichi Suzuki(1898 1998) fue violinista, educador, filósofo y fue considerado
					un humanista. Ejerció una profunda influencia en la educación de su país y en
					otros lugares del mundo. <br /><br />
					Suzuki basó su enfoque en la hipótesis de que la habilidad musical no es un talento innato, sino una
					destreza que todos los niños  pueden desarrollar."Cualquier niño a quien se entrene correctamente 
					puede desarrollar una habilidad musical".<br /><br />
					El Dr. Suzuki no desarrolló su método para entrenar a músicos profesionales, sino para ayudar a los
					niños a desarrollar sus capacidades como seres humanos. Él dijo: "La enseñanza de música no es mi 
					propósito principal. Deseo formar a buenos ciudadanos, seres humanos nobles. Si un niño oye buena
					música desde el día de su nacimiento, y aprende a tocarla él mismo, desarrollará sensibilidad,  
					disciplina y paciencia. Adquiere un corazón hermoso".
				</div>
				
				
				
				<h3 class="gris3">Características</h3>
				
				<div class="texto gris2">
					<strong>Importancia del papel de los padres.</strong> Cuando un niño aprende a hablar, los 
					padres actúan eficazmente como profesores. Los padres también tienen un 
					papel importante como "profesores en el hogar" cuando el niño aprende a 
					tocar un instrumento.
					<br />
					<br />
					<strong>Comienzo temprano.</strong> Los primeros años son cruciales en el desarrollo de los procesos mentales 
					y de coordinación muscular en el niño pequeño. El escuchar música debe comenzar en el nacimiento 
					y el entrenamiento formal puede comenzar a la edad de tres o cuatro años.
					<br /> 
					<br />
					<strong>La escucha</strong>. Los niños aprenden a hablar en un ambiente lleno de estímulos de lenguaje. 
					Los padres pueden también hacer que la música forme parte del ambiente del niño, Repetición.
					Cuando los niños han aprendido una palabra no la dejan, sino que continúan utilizándola
					a la vez que agregan nuevas palabras a su vocabulario. De igual modo, los estudiantes 
					Suzuki repiten las piezas que aprenden.
					<br />
					<br />
					<strong>Alentar.</strong> Al igual que con el lenguaje, los esfuerzos del niño para aprender a 
					tocar un instrumento se han de elogiar con sinceras palabras de aliento. Cada 
					niño aprende a su propio paso, avanzando a pequeños pasos para poder dominar 
					cada uno de ellos. Aprender con otros niños. La música promueve interacciones 
					sociales sanas, y la participación en lecciones de grupos y pequeños conciertos, 
					además de sus propias lecciones individuales, motiva a los niños en gran medida. 
					Disfrutan al observar otros niños en diversos niveles, aspirando llegar al nivel 
					de los estudiantes más avanzados.
					<br />
					<br />
					<strong>Repertorio gradual.</strong> Los niños no practican ejercicios para aprender a hablar, 
					sino que aprenden usando el lenguaje para comunicarse y expresarse. Con el método 
					Suzuki, los estudiantes aprenden conceptos y habilidades musicales en el contexto 
					de la música. Posponer la lectura. A los niños no se les enseña a leer sino hasta 
					que su capacidad para hablar ha quedado bien consolidada. De la misma manera, los 
					estudiantes Suzuki han de alcanzar un nivel de destreza básico tocando su instrumento 
					antes de que se les enseñe a leer música. 
					
				</div>
				
